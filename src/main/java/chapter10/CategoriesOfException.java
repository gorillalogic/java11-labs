package chapter10;

public class CategoriesOfException {


    /*
        An exception is an event that alters program flow.
        Java has a Throwable superclass for all objects that represents these events.
        Not all of them have the word *exception* in their class name, which can be
        confusing.
     */

    /*
           See image CategoriesOfException.jpg
           Error means something went so horribly wrong that your program should not
           attempt to recover from it. For example, the disk drive "disappeared" or
           the program ran out of memory. These are abnormal conditions that you aren't
           likely to encounter and cannot recover from.
     */
}
